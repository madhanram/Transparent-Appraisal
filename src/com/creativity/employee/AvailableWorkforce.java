package com.creativity.employee;

public class AvailableWorkforce {
private int employee_id;
private String employeeName;
private String skillSet;
private int workExp;
private String ifTagged;
private String ifManager;
public int getEmployee_id() {
	return employee_id;
}
public void setEmployee_id(int employee_id) {
	this.employee_id = employee_id;
}
public String getEmployeeName() {
	return employeeName;
}
public void setEmployeeName(String employeeName) {
	this.employeeName = employeeName;
}
public String getSkillSet() {
	return skillSet;
}
public void setSkillSet(String skillSet) {
	this.skillSet = skillSet;
}
public int getWorkExp() {
	return workExp;
}
public void setWorkExp(int workExp) {
	this.workExp = workExp;
}
public String getIfTagged() {
	return ifTagged;
}
public void setIfTagged(String ifTagged) {
	this.ifTagged = ifTagged;
}
public String getIfManager() {
	return ifManager;
}
public void setIfManager(String ifManager) {
	this.ifManager = ifManager;
}
}
